            <p>Capitale de l'ancienne province du Berry, à quelques dizaines de kilomètres du centre géométrique de la France métropolitaine, et à 240 km au sud de Paris, la ville de Bourges est située à la confluence de plusieurs rivières (Yèvre et Voiselle, une de ses dérivations, Auron, Moulon, Langis). Cette forte présence de l'eau affluant dans une vallée (celle de l'Yèvre) à la pente très peu marquée explique l'importante surface marécageuse au pied de la ville médiévale (actuel centre ville, sur un promontoire rocheux en prolongement du plateau Sud). Ces vastes zones humides cernant autrefois la ville à l'Est et l'Ouest et pendant longtemps dédiées au pacage et au maraîchage ont au fur et à mesure du développement urbain été aménagées par les humains : canalisation des cours d'eau, remblais, urbanisation, exploitation agricole. Les marais de l'Yèvre et de la Voiselle divisés en multiples parcelles et dédiés aujourd'hui à l'exploitation potagère d'agrément et aux loisirs ont au sein de cet ensemble géographique spécifique peu subi l'urbanisation.</p>
<p>Ce n'est que tardivement dans les années 1970, qu'un lac artificiel a été créé au sud de la ville par l'établissement d'un barrage sur l'Auron, qui est maintenant le centre d'une expansion urbanistique de la Ville vers le Sud.
La ville de Bourges est malgré tout désavantagée par sa situation géographique, qui n'offre aucune voie principale de communication aussi bien ferroviaire que routière. Elle dispose malgré tout d'un grand patrimoine historique comme l'Avaricum, la cathédrale Saint-Étienne de Bourges classée au Patrimoine mondial de l'UNESCO, ou encore la Maison de la culture de Bourges.</p>
<p>Ancienne ville fortifiée, Bourges témoigne encore aujourd'hui de ces marques avec la présence notamment au niveau de la mairie de remparts gallo-romains ainsi que le marquage au sol d'une ancienne tour.</p>
                
